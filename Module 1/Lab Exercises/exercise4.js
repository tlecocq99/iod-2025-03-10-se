function sum(a, b) {
  return a + b;
}
console.log(sum(7, 3)); // 10
function multiply(a, b) {
  return a * b;
}
console.log(multiply(2, 4)); // 8
function divide(a, b) {
  return a / b;
}
console.log(divide(12, 3)); // 4
function subtract(a, b) {
  return a - b;
}
console.log(subtract(10, 5)); // 5

var myName = "Thomas";
console.log("Hello " + myName); // Hello Thomas
